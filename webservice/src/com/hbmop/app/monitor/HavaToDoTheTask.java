package com.hbmop.app.monitor;

public class HavaToDoTheTask {
	private String ExecutionId;
	private String name;//名称
	private String taskInitiator;//任务发起者
	private String receiveTime;//任务接收时间
	private String completionTime;//完成时间
	private String processNode;//流程节点
	private String processStatus;//流程状态
	public String getExecutionId() {
		return ExecutionId;
	}
	public void setExecutionId(String executionId) {
		ExecutionId = executionId;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getTaskInitiator() {
		return taskInitiator;
	}
	public void setTaskInitiator(String taskInitiator) {
		this.taskInitiator = taskInitiator;
	}
	public String getReceiveTime() {
		return receiveTime;
	}
	public void setReceiveTime(String receiveTime) {
		this.receiveTime = receiveTime;
	}
	public String getCompletionTime() {
		return completionTime;
	}
	public void setCompletionTime(String completionTime) {
		this.completionTime = completionTime;
	}
	public String getProcessNode() {
		return processNode;
	}
	public void setProcessNode(String processNode) {
		this.processNode = processNode;
	}
	public String getProcessStatus() {
		return processStatus;
	}
	public void setProcessStatus(String processStatus) {
		this.processStatus = processStatus;
	}
	
}
